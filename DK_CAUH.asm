A2 BIT P3.3
B1 BIT P3.4
B2 BIT P3.5

BEGIN:
SETB A1
SETB A2
SETB B1
SETB B2
MAIN:
CLR A1
CLR A2
CALL DELAY
SETB A1
SETB A2
CLR B1
CLR B2
