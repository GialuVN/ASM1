ORG	30H
QUET: JNB P2.0,LED1	
      JNB P2.1,LED2
      LJMP QUET
           
LED1: SETB P0.0
		CLR P0.1
		SJMP KTRA
		 RET
LED2: SETB P0.1
		CLR P0.0
		RET
TAT: SETB P2.0
	 SETB P2.1
		RET
TIMER: MOV TMOD,#1
     MOV TH0,#100
     MOV TH1,#100
 	RET
KTRA: ACALL TIMER	;GOI BO DINH THOI
		CLR TF0 ;XOA CO
		SETB TR0 ;BAT DAU DEM
LAP: JB P2.0,QUET
	 JB TF0,TAT
	 SJMP LAP

	RET
	END
		