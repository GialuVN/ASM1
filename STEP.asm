ORG 00
mov p3,#0
call delay
MAIN:
	JMP HAFT
	MOV P3,#09H
	CALL DELAY
	MOV P3,#0CH
	CALL DELAY
	MOV P3,#06H
	CALL DELAY
	MOV P3,#03H
	CALL DELAY
	JMP $
HAFT:
	MOV P3,#09H
	CALL DELAY
	MOV P3,#08H
	CALL DELAY
	MOV P3,#0CH
	CALL DELAY
	MOV P3,#04H
	CALL DELAY
	MOV P3,#06H
	CALL DELAY
	MOV P3,#02H
	CALL DELAY
	MOV P3,#03H
	CALL DELAY
	MOV P3,#01H
	CALL DELAY
	JMP $
DELAY:
		PUSH 00H
		PUSH 01H
		PUSH 02H
		MOV R0,#50
REP2_DELAY:
		MOV R1,#100
REP1_DELAY:
		MOV R2,#100
		DJNZ R2,$
		DJNZ R1,REP1_DELAY
		DJNZ R0,REP2_DELAY
		POP 02H
		POP 01H
		POP 00H
		RET
		END