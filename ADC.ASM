ADCA	BIT	P3.0
ADCB	BIT	P3.1
ADCC	BIT	P3.2
ALE		BIT	P3.3
START	BIT	P3.4
EOC	BIT	P3.5
ORG 00
MAIN:
CALL DELAY
SETB EOC
CLR ALE				;ALE MUC THAP
CLR START			;ALE MUC CAO
CLR ADCA			;CHON KENH
CLR ADCB
CLR ADCC
CALL DELAY
SETB ALE			;CHOT DIA CHI
CALL DELAY
SETB START
CALL DELAY
CLR ALE
CALL DELAY
CLR START
JNB EOC,$
MOV P2,P1
JMP MAIN




DELAY:
		MOV R1,#10
		DJNZ R1,$
		RET















