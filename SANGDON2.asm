ORG 00H
MAIN:
	MOV 40H,#8
	MOV R5,#41H
LAP2:
	MOV A,#42H
	MOV R6,40H
LAP:
	PUSH ACC
	ANL A,R5
	CPL A
	MOV P2,A
	MOV P1,A
	MOV P0,A
	POP ACC
	RL A
	CALL DELAY250MS
	DJNZ R6,LAP
	DEC 40H
	MOV A,R5
	CLR C
	RRC A
	MOV R5,A
	CJNE A,#0,LAP2
	MOV 40H,#8
	MOV R5,#41H
LAP3:
	MOV A,#42H
	MOV R6,40H
LAP4:
	PUSH ACC
	ANL A,R5
	CPL A
	MOV P2,A
	MOV P1,A
	MOV P0,A
	POP ACC
	RR A
	CALL DELAY250MS
	DJNZ R6,LAP4
	DEC 40H
	MOV A,R5
	CLR c
	RLC A
	MOV R5,A
	CJNE A,#0,LAP3
	JMP MAIN
	
DELAY250MS:
	MOV R0,#255
RE_DELAY:
	MOV R1,#255
	DJNZ R1,$
	DJNZ R0,RE_DELAY
	RET
	END
	
