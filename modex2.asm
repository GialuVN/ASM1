ORG 0
MAIN:
		MOV A,#0FEH
LOOP:
		MOV P0,A
		MOV P0,#0FFH
		CALL DELAY
		MOV P0,A
		CALL DELAY
		RL A
		CJNE A,#0FEH,LOOP
		
		
		MOV A,#07FH
LOOP2:
		MOV P0,A
		CALL DELAY
		RR A
		CJNE A,#07FH,LOOP2
		JMP MAIN		
DELAY:
		MOV R0,#10
LAP:
		MOV R1,#100
LAP1:
		MOV R2,#100
		DJNZ R2,$
		DJNZ R1,LAP1
		DJNZ R0,LAP
		RET
		END
		