ORG 00H
SJMP MAIN
ORG	30H

MAIN: MOV A,#0FEH
	MOV	P0,A
	RLC A
	ACALL	DELAY
	CJNE A,#0FEH,MAIN
MAIN1: MOV A,#0FEH
	MOV	P1,A
	RLC A
	ACALL	DELAY
	CJNE A,#0FEH,MAIN1
MAIN2: MOV A,#0FEH
	MOV	P2,A
	RLC A
	ACALL	DELAY
	CJNE A,#0FEH,MAIN2
MAIN3: MOV A,#0FEH
	MOV	P3,A
	RLC A
	ACALL	DELAY
	CJNE A,0FEH,MAIN3
	SJMP MAIN
DELAY:
		MOV	TMOD,#01H
		MOV	R0,#20
BACK:	MOV	TH0,#0C3H
		MOV	TL0,#50H
		SETB	TR0
WAIT:JNB TF0,WAIT
	CLR	TF0
	CLR	TR0
	DJNZ	R0,BACK
	RET
END