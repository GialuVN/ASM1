;KHAI BAO BIEN
MASK DATA 30H
BUF  DATA 31H
BIT_CONTR  DATA 20H
CLOCK   BIT BIT_CONTR.0


ORG 00H
JMP MAIN
ORG 0BH
JMP INT_T0
MAIN:
	MOV IE,#82H			;NGAT DO TIMER 0
	MOV TMOD,#02H		;TIMER 0 CHE DO 2
	SETB TF0
LOOP:	
	MOV A,#255
	CALL DELAY
	MOV A,MASK
	ORL P2,A
	
	JMP LOOP
	
	
	


















DELAY:
	PUSH 07H
	MOV R7,A
L_DELAY:
	JNB CLOCK,$
	CLR CLOCK
	DJNZ R7,L_DELAY
	POP 07H
	RET



INT_T0:
	MOV TL0,#-255
	MOV TH0,#-255
	SETB TR0
	SETB CLOCK
	RETI
	
	END