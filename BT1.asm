ORG 0
MAIN:
	MOV R1,#4
LAP3:
	MOV A,#01H
	MOV R0,#8
LAP:
	MOV P0,A
	RL A
	CALL DELAY
	DJNZ R0,LAP
	
	MOV A,#80H
	MOV R0,#8
LAP2:
	MOV P0,A
	RR A
	CALL DELAY
	DJNZ R0,LAP2
	DJNZ R1,LAP3
	MOV R0,#8
LAP4:
	MOV P0,#255
	CALL DELAY
	MOV P0,#0
	CALL DELAY
	DJNZ R0,LAP4
