ORG 00H
MAIN:
	MOV 40H,#8
	MOV R7,#0FFH
LAP2:
	MOV A,#0FEH
	MOV R6,40H
LAP:
	PUSH ACC
	ANL A,R7
	MOV P0,A
	POP ACC
	RL A
	CALL DELAY
	DJNZ R6,LAP
	DEC 40H
	MOV A,R7
	CLR C
	RRC A
	MOV R7,A
	CJNE A,#0,LAP2
	MOV 40H,#8
	MOV R7,#0FFH
LAP3:
	MOV A,#0FEH
	MOV R6,40H
LAP4:
	PUSH ACC
	ANL A,R7
	MOV P0,A
	POP ACC
	RR A
	CALL DELAY
	DJNZ R6,LAP4
	DEC 40H
	MOV A,R7
	CLR c
	RLC A
	MOV R7,A
	CJNE A,#0,LAP3
	JMP MAIN
	
DELAY:
	MOV R0,#255
RE_DELAY:
	MOV R1,#255
	DJNZ R1,$
	DJNZ R0,RE_DELAY
	RET
	END
