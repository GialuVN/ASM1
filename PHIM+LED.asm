ORG 00
DV EQU 31H
SETB P2.7
MAIN:
		MOV DV,#1
		CALL HT
		JMP MAIN
		
HT:		PUSH 02H
		MOV R2,#2
		MOV A,DV
		MOV DPTR,#DULIEU
		MOVC A,@A+DPTR
		MOV P2,A
		CPL P2.7
		CALL DELAY
		CPL P2.7
		MOV P2,#00H
		POP 02H
		RET
DELAY:
		POP 01H
		POP 00H
		MOV R0,#200
LOOP1:
		MOV R1,#200
		DJNZ R1,$
		DJNZ R0,LOOP1
		POP 00H
		POP 01H
		RET
DULIEU:
		DB 3FH,06H,5BH,4FH,66H,6DH,7DH,07H,7FH,6FH
		END