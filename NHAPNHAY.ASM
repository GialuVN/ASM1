


ORG 00H
SOLAN EQU 2
	LAP1:
		MOV P0.1,00H
		CALL DELAY
		MOV P0.2,00H
		CALL DELAY
		RET		
DELAY:	
		PUSH 00H
		MOV R0,#SOLAN
		MOV TMOD,#01H
LAP:
		MOV TL0,#LOW(-50000)
		MOV TH0,#HIGH(-50000)
		SETB TR0
		JNB TF0,$
		CLR TR0
		CLR TF0
		DJNZ R0,LAP
		POP 00H
		RET
		END