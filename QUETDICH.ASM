ORG 0
MAIN:
MOV SCON,#00H 	;CHE DO THANH GHI DICH
MOV DPTR,#DULIEU









DISP:
	MOV R1,#2
	MOV R2,#1
LOOP:
	MOV A,R2
	CALL SEND
	MOV A,R1
	MOVC A,@A+DPTR
	CALL SEND
	SETB P3.5
	CLR P3.4
	SETB P3.4
	CLR P3.5
	CALL DELAY
	MOV A,R2
	RL A
	MOV R2,A
	INC R1
	CJNE A,#1,LOOP
	JMP DISP
SEND:
	MOV SBUF,A
	JNB TI,$
	CLR TI
	RET

	DELAY:
		PUSH 01H
		PUSH 02H
		MOV R1,#30
	RE_DELAY:
		MOV R2,#100
		DJNZ R2,$
		DJNZ R1,RE_DELAY
		POP 02H
		POP 01H
		RET
DULIEU:
	DB 0FCH,60H,0DAH,0F2H,66H,0B6H,0BEH,0E0H,0FCH,0F6H
END