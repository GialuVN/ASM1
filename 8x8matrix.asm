CALL DELAY
SETB P2.0
MOV P0,#080H
CLR P2.1
CALL DELAY
SETB P2.1
MOV P0,#0BFH
CLR P2.2
CALL DELAY
SETB P2.2
MOV P0,#0CFH
CLR P2.3
CALL DELAY
SETB P2.3
